package section02;

public class IntegerManipulations {
    public static void main(String[] args) {
        int a;
        a = 5;
        int b = 7;
        int c = a + b;
        c = c + 5;
        c = c * 10;
        a = 10;
        b = 3;
        c += 5;
        c *= 2;
        System.out.println("c = " + c);
        System.out.println("a / b = " + a / b);
        System.out.println("a % b = " + a % b);
    }
}
